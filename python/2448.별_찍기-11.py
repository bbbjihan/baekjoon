import sys;rl=sys.stdin.readline
from math import log2
import copy

N = int(rl())
k = 1 if N == 3 else int(log2(N // 3)) + 1

now = ["  *  ", " * * ", "*****"]
prev = []

for i in range(1, k):
  prev = copy.deepcopy(now)
  n = pow(2, i) * 3
  blink = " " * (n // 2)
  now = []
  
  for j in range(n):
    prevLine = prev[j % len(blink)]
    
    if j < len(blink):
      now.append(blink + prevLine + blink)
    else:
      now.append(prevLine + " " + prevLine)

for line in now:
  print(line)

